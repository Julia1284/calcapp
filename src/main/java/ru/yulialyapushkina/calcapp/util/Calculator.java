package ru.yulialyapushkina.calcapp.util;

public class Calculator {
    public static int calcSum(int num1, int num2) {
        return num1 + num2;
    }

    public static int calcDiff(int num1, int num2) {
        return num1 - num2;
    }

    public static int calcProd(int num1, int num2) {
        return num1 * num2;
    }
}
